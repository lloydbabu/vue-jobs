<script setup lang="ts">
/**
 * Import the logo image and necessary functions from vue-router.
 */
import logo from '@/assets/img/logo.png'
import { RouterLink, useRoute } from 'vue-router'

/**
 * Function to check if the given route path is active.
 * @param routePath - The path of the route to check.
 * @returns True if the route is active, false otherwise.
 */
const isActiveLink = (routePath: string) => {
  const route = useRoute()
  return route.path === routePath;
};
</script>

<template>
  <!-- Navigation bar component -->
  <nav class="bg-green-700 border-b border-green-500">
    <div class="mx-auto max-w-7xl px-2 sm:px-6 lg:px-8">
      <div class="flex h-20 items-center justify-between">
        <div class="flex flex-1 items-center justify-center md:items-stretch md:justify-start">
          <!-- Logo -->
          <a class="flex flex-shrink-0 items-center mr-4" href="index.html">
            <img class="h-10 w-auto" :src="logo" alt="Vue Jobs" />
            <span class="hidden md:block text-white text-2xl font-bold ml-2">Vue Jobs</span>
          </a>
          <div class="md:ml-auto">
            <div class="flex space-x-2">
              <!-- Router links for navigation -->
              <RouterLink
                to="/"
                :class="[ isActiveLink('/') ? 'bg-green-900' : 'hover:bg-gray-900 hover:text-white', 'text-white', 'rounded-md', 'px-3', 'py-2']"
                >Home
              </RouterLink>
              <RouterLink
                to="/jobs"
                :class="[ isActiveLink('/jobs') ? 'bg-green-900' : 'hover:bg-gray-900 hover:text-white', 'text-white', 'rounded-md', 'px-3', 'py-2']"
                >Jobs
              </RouterLink>
              <RouterLink
                to="/jobs/add"
                :class="[ isActiveLink('/jobs/add') ? 'bg-green-900' : 'hover:bg-gray-900 hover:text-white', 'text-white', 'rounded-md', 'px-3', 'py-2']"
                >Add Job
              </RouterLink>
            </div>
          </div>
        </div>
      </div>
    </div>
  </nav>
</template>
